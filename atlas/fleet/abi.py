# A7 protocol/ABI hash — derived from intentproto's spec-frozen core ids.
#
# The wire contract every board and the host must agree on is the set of
# spec-frozen core message ids in intentproto (core_ids.hpp).  Hashing
# that canonical allocation gives one stable "protocol hash" that is the
# single version authority: bake it into every image and the host, and a
# handshake mismatch means "not in lockstep" — deterministically, with no
# judgement call.  Because the ids are spec-frozen (not per-build), the
# hash changes only when the contract itself changes.
#
